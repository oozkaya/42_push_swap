#include "libft.h"

intmax_t	ft_atoi(const char *str)
{
	int			i;
	int			signe;
	intmax_t	res;

	res = 0;
	signe = 1;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
			|| str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		signe = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (res > 9223372036854775807)
		return (signe < 0 ? 0 : -1);
	return (res * signe);
}
