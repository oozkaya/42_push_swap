#include "libft.h"

void	ft_putnbr(int n)
{
	long	nbr;

	nbr = n;
	if (n < 0)
	{
		nbr = -nbr;
		ft_putchar('-');
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + '0');
}
